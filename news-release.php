    <p>FOR IMMEDIATE RELEASE
    <br />Rosebud Sioux Tribe to take a Lead on Protecting Sacred Site</p>
    
    <p>ROSEBUD, S.D. - In the spirit of religious freedom, the Rosebud Sioux Tribal Council announced Wednesday, Aug. 8 that it would act as the fiscal agent to bid in the Pe Sla sacred site auction in the Black Hills. The site, also known as the Reynolds Prairie Ranches, between Lead and Hill City, S.D., will be up for bidding on Aug. 25.</p>
    
    <p>The Pe Sla sacred site is where, according to the Lakota spiritual tradition, the Morning Star fell to earth, killing sever beings that killed seven women. To honor the fallen, the Morning Star placed the souls of the women into the stars at the Pleiades constellation. The geographic distinction of the tradition is noted as the translation, &ldquo;Old Baldy,&rdquo; because of the stark contrast from the surrounding hills and trees to open prairie.</p>
    
    <p>Every year, tribes within the Lakota, Dakota, and Nakota nations gather to perform ritual and ceremony as a way to ensure the continued wellbeing of all people. The Rosebud Sioux Tribe is amoung those tribes that send contingents to there every year. In that spirit, the Rosebud council voted to act as the conduit to unite all the Oceti Sakowin - or Seven Council Fires of the Nation - in this stuggle to maintain its deeply rooted traditions. If successful, this will mark one of the first attempts by the Great Sioux Nation to unite in common cause, bid for ownership and shar the site for all tribal members to practice their faith.</p>
    
    <p>The tribe is working with <a href="http://LastRealIndians.com">LastRealIndians.com</a> to accept donations from both tribes and individuals, who want to join in keeping religious freedoms for Lakota people alive and intact at Pe Sla. Send contributions to: Rosebud Sioux Tribe / Pe Sla, 11 Legion Ave., P.O. Box 430, Rosebud, SD 57570 or online with LRI at: <a href="http://indiegogo.com/PeSla-LakotaHeartland">http://indiegogo.com/PeSla-LakotaHeartland</a>. All donations to the Tribe are tax-deductible and will only be used toward the purchase of Pe Sla.</p>
    
